CREATE TABLE REFERENCE_SOURCE (
	REFERENCE_SOURCE_ID VARCHAR(255) NOT NULL PRIMARY KEY,
	REFERENCE_SOURCE VARCHAR(255) NOT NULL,
	INACTIVE TINYINT(1)
);  

CREATE TABLE REFERENCE_TYPE (
	REFERENCE_TYPE_ID VARCHAR(255) NOT NULL PRIMARY KEY,
	REFERENCE_SOURCE_ID VARCHAR(255) NOT NULL,
	REFERENCE_TYPE VARCHAR(255) NOT NULL,
	CONSTRAINT 'FK_REFERENCE_TYPE_REFERENCE_SOURCE_ID' FOREIGN KEY (REFERENCE_SOURCE_ID) REFERENCES REFERENCE_SOURCE (REFERENCE_SOURCE_ID)
);

CREATE TABLE REFERENCE (
	REFERENCE_ID VARCHAR(255) NOT NULL PRIMARY KEY,
	REFERENCE_TYPE_ID VARCHAR(255) NOT NULL,
	NATIVE_SOURCE_ID_VALUE VARCHAR(255) NOT NULL,
	MASTER_ID VARCHAR(255) NOT NULL,
	CONSTRAINT 'FK_REFERENCE_REFERENCE_TYPE_ID' FOREIGN KEY (REFERENCE_TYPE_ID) REFERENCES REFERENCE_TYPE (REFERENCE_TYPE_ID)
);